package com.mobileapp.models

class Word {
    var id: Int
        get() = id

        set(value) {
            id = value
        }

    var word: String

        get() = word

        set(value) {
            word = value
        }

    var translatedWord: String

        get() = translatedWord

        set(value) {
            translatedWord = value
        }

}